class info:
    def __int__(self):
        self.rows = 0
        self.cols = 0
        self.cost = 0
        self.path = 0
        self.time = ""

    def get_rows(self):
        return self.rows

    def get_cols(self):
        return self.cols

    def get_cost(self):
        return self.cost

    def get_path(self):
        return self.path

    def get_time(self):
        return self.time

    def set_rows(data):
        rows = data

    def setCols(input):
        cols = input

    def setCost(input):
        cost = input

    def setPath(input):
        path = input

    def setTime(input):
        time = input
